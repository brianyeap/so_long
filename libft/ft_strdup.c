#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*mem;

	i = 0;
	while (s1[i])
		i++;
	mem = malloc(i * sizeof(char) + 1);
	if (!mem)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		mem[i] = s1[i];
		i++;
	}
	mem[i] = '\0';
	return (mem);
}
